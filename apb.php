<p class="sous_titre">A) Présentation</p>
   <p>    Le B.D.E est l'acronyme de bureau des élèves<br><br>
          Mais qu'est-ce-que le bureau des élèves ?<br><br>
          Le bureau des élèves est une association à but non lucratif qui a pour mission principale : <br>
          l'organisation d'événements et/ou d'activités rentrants dans le cadre de la vie associative du CESI.<br>
          Le CESI est un centre de formations qui propose plusieurs formations et diplômes pour des futurs cadres en entreprise.<br><br>
          La pluridisplinarité au sein du CESI est l'essor de la diversité et de cohésion que l'on retrouve dans une entreprise. <br>
          Elle permet aux étudiants, la création d'un réseau à l'intérieur de l'école, d'où l'importance d'un BDE CESI.<br>
          Avant 2016, le BDE était une exclusivité des internes du CESI. Il était majoritairement représenté par les Exars.<br>
          Á la rentrée 2016, L'ancien BDE est dissout et laisse place à un nouveau BDE CESI.<br>
          Ce changement donne un nouveau souffle au BDE. C'est ainsi que cette cohésion recherchée peut aboutir.<br><br>
          
          Nous, Membre du BDE, jurons solennellement de servir au mieux les intérêts des étudiants du CESI.<br>
          Et jurons de vous, étudiants, proposer des évenements qui vous permettront d'accroitre votre réseau,<br> 
          tout en assurant votre divertissement.<br>
          </p>
